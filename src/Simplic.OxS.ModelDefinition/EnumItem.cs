namespace Simplic.OxS.ModelDefinition;

/// <summary>
/// Represents entries fo an enum.
/// </summary>
public class EnumItem
{
    /// <summary>
    /// Gets or sets the name of the entry.
    /// </summary>
    public string Name { get; set; }

    /// <summary>
    /// Gets or sets the value.
    /// </summary>
    public long Value { get; set; }
}